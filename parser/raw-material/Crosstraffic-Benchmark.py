def setupNetwork():
    "Create network and run simple performance test"
    topo = BenchmarkTopo()
    net = Mininet(topo=topo, controller=lambda a: RemoteController( a, ip='10.0.2.2', port=6633 ), host=CPULimitedHost, link=TCLink)
    return net

